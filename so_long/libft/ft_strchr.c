char	*ft_strchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (s[n] != '\0')
	{
		if (s[n] == (char)c)
			return ((char *)&s[n]);
		n++;
	}
	if (c == '\0')
		return ((char *)&s[n]);
	return (0);
}
